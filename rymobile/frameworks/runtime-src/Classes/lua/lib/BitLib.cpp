#include "BitLib.h"

/************************************************************************/
/* 与运算                                                               */
/************************************************************************/
int LuaBitLib::bAnd(int a, int b)
{
	return a & b;
}

/************************************************************************/
/* 或运算                                                               */
/************************************************************************/
int LuaBitLib::bOr(int a, int b)
{
	return a | b;
}

/************************************************************************/
/* 左移运算符                                                            */
/************************************************************************/
int LuaBitLib::lShift(int a, int b)
{
	return a << b;
}

/************************************************************************/
/* 右移运算符                                                            */
/************************************************************************/
int LuaBitLib::rShift(int a, int b)
{
	return a >> b;
}

/************************************************************************/
/* 取反运算符                                                            */
/************************************************************************/
int LuaBitLib::bNot(int a)
{
	return ~a;
}